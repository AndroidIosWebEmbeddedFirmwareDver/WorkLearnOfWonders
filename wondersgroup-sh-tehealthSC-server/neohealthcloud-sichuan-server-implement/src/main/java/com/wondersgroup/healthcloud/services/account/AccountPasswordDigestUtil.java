public final class AccountPasswordDigestUtil {
    private static final String salt = "eps-salt@20160217";

    public static String digest(String password) {
        if (StringUtils.isBlank(password)) {
            return null;
        }
        String saltAppendedPassword = password + salt;
        return DigestUtils.md5DigestAsHex(saltAppendedPassword.getBytes());
    }
}
