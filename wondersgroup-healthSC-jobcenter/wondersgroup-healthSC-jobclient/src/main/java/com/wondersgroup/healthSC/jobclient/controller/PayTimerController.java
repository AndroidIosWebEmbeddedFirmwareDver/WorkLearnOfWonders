@RestController
@RequestMapping("/pay/schedule")
public class PayTimerController {

    @Autowired
    private PayScheduleTimer timer;

    @RequestMapping(value = "/timer", method = RequestMethod.POST)
    public String setTimer(@RequestBody String body) {
        JsonKeyReader reader = new JsonKeyReader(body);
        String id = reader.readString("id", false);
        String status = reader.readString("status", false);
        Long triggerTime = Long.valueOf(reader.readString("time", false));

        timer.setTimer(id, status, triggerTime);


        return "{\"code\":0}";
    }
}
