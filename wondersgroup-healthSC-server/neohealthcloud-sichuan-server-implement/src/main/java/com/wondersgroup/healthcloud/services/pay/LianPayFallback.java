@Component
public class LianPayFallback {

    private static final String prefix = "sc:order:fail:";
    private static final int expire = 24 * 60 * 60;

    @Autowired
    private JedisPool pool;

    /**
     * @param orderId
     * @return need to refund
     */
    public Boolean markOnFailure(String orderId) {
        try (Jedis jedis = pool.getResource()) {
            String key = prefix + orderId;
            Long count = jedis.incr(key);
            if (count >= 3L) {
                return true;
            } else {
                jedis.expire(key, expire);
                return false;
            }
        }
    }
}
