public class RequestWrapperFilter implements Filter {

    private static final boolean SHOW_MSG = true;

    @Override
    public void init(FilterConfig filterConfig) throws ServletException {

    }

    @Override
    public void doFilter(ServletRequest servletRequest, ServletResponse servletResponse, FilterChain filterChain) throws IOException, ServletException {
        MultiReadHttpServletRequest wrapped = new MultiReadHttpServletRequest(servletRequest);
        if (SHOW_MSG) {
            System.out.println("\nGet a request:   " + wrapped.getRequestURI() + "\n");
        }
        filterChain.doFilter(wrapped, servletResponse);
    }

    @Override
    public void destroy() {

    }
}
